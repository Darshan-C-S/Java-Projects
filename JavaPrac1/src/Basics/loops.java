package Basics;

public class loops {
    public static void main(String[] args) {
        int i = 1;
        while(i<=10)
        {
            System.out.println(i);
            i++;
        }
//        to find series of even no with only looping
        for (int j = 0; j < 10; j= j+2) {
            System.out.println(j);
        }
    }
}
